package 第2回課題;

public class Task2 {
	public static void main(String[] args) {
		
		//変数greetingをString型で定義して、Hello Worldを出力
		String greeting;
		greeting = "Hello World";
		System.out.println(greeting);
	
		//1から10を足した結果を出力
		int sum = 0;
		for (int i = 1; i <= 10; i++) {
			sum = sum + i;
		}
			System.out.println(sum);
		
	}

}